    private void injectresultActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_injectresultActionPerformed
        // To inejct result into the database :
        try {
            // To book tickets and store data in DB:
            String rollNo = rollno.getText();
            String mathS = maths.getText();
            String dsA = dsa.getText();
            String oS = os.getText();
            String dbmS = dbms.getText();
            String projecT = project.getText();
            
            int value = Integer.parseInt(maths.getText()) + Integer.parseInt(dsa.getText()) + Integer.parseInt(os.getText()) + Integer.parseInt(dbms.getText()) + Integer.parseInt(project.getText());
            String totalmarkS = Integer.toString(value);
            
            String decisioN = "";
            if(value >= 65)
                decisioN = "Pass";
            else
                decisioN = "Fail";
            
            prestate = connection.prepareStatement("insert into studentresult (rollno, maths, dsa, os, dbms, project, marksobtained, grade, decision)values(?,?,?,?,?,?,?,?,?)");
            prestate.setString(1, rollNo);
            prestate.setString(2, mathS);
            prestate.setString(3, dsA);
            prestate.setString(4, oS);
            prestate.setString(5, dbmS);
            prestate.setString(6, projecT);
            prestate.setString(7, totalmarkS);
            
            if(value >= 92)
                prestate.setString(8, "10");
            else if(value >= 89 && value < 92)
                prestate.setString(8, "9");
            else if(value >= 78 && value < 89)
                prestate.setString(8, "8");
            else if(value >= 65 && value < 78)
                prestate.setString(8, "7");
            else 
                prestate.setString(8, "5");
               
            prestate.setString(9,decisioN);
            
            // IMPORTANT EXECUTE FUNCTION
            prestate.executeUpdate();
            JOptionPane.showMessageDialog(null,"Injection Successful !");
        } catch (SQLException ex) {
            Logger.getLogger(AdminWindow.class.getName()).log(Level.SEVERE, null, ex);
        }
    }//GEN-LAST:event_injectresultActionPerformed

