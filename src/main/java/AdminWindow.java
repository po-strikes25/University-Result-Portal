    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // To display results in the table:
        if(btech.isSelected() && barch.isSelected())
            JOptionPane.showMessageDialog(null,"Please select one course");
        // Bifurcate this code late into btech and barch 
        else {
            try {
            int columns;
            
            //The above established connection helps to create statement
            java.sql.Statement statement = connection.createStatement();
            
            prestate = connection.prepareStatement("select * from studentresult");
//            prestate.setString(1, rollno);
            
            //To get result and display on the table
            ResultSet res = prestate.executeQuery();
            
            // Helps to push flight fetched data on the UI Table
            ResultSetMetaData resMetaData = res.getMetaData();
            
            //Count of Total columns
            columns = resMetaData.getColumnCount();
            
            //In which model we want to display the table contents
            DefaultTableModel defTableModel = (DefaultTableModel) resulttable.getModel();
            
            //0th row
            defTableModel.setRowCount(0);
            
            // Writing over the table
            while(res.next()){
                
                @SuppressWarnings("UseOfObsoleteCollectionType")
                Vector v2 = new Vector();
                
                for(int i=1 ; i <= columns ; i++){
                    v2.add(res.getString("rollno"));
                    if(btech.isSelected())
                        v2.add("B.Tech");
                    else
                        v2.add("B.Arch");
                    v2.add(res.getString("marksobtained"));
                    v2.add(res.getString("grade"));
                    v2.add(res.getString("decision"));
                }
                defTableModel.addRow(v2);
            }
        } catch (SQLException ex) {
            Logger.getLogger(AdminWindow.class.getName()).log(Level.SEVERE, null, ex);
        }
        }
    }//GEN-LAST:event_jButton1ActionPerformed

