    private void viewstudentActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_viewstudentActionPerformed
        // When view button selected :
        viewstudentdetails.setVisible(true);
        viewtermresults.setVisible(false);
        
        /*  Error was occuring as I had written this code above the try-catch block;
            Transfering the RollNo Entered by the Student to the StudentWindow Panel(s): */
            String roLLno = StudentLogin.rollno.getText();
        
        // To display all the details of the student in the textfields :
        try {
            
            prestate = connection.prepareStatement("select * from studentdata where rollno = ?");
            prestate.setString(1, roLLno);
            
            ResultSet res = prestate.executeQuery();
            res.next();
            
            name.setText(" " + res.getString("firstName") + " " + res.getString("lastName"));
            email.setText(" " + res.getString("email"));
            course.setText(" " + res.getString("course"));
            branch.setText(" " + res.getString("branch"));
            contactno.setText(" " + res.getString("contactno"));
            
        } catch (SQLException ex) {
            Logger.getLogger(StudentWindow.class.getName()).log(Level.SEVERE, null, ex);
        }
    }//GEN-LAST:event_viewstudentActionPerformed

